let array1 = [1, 2, 3, 4, 5]

function reverseArray(array) {
  let len = array.length
  for(let i = 0; i < Math.floor(len/2); i++){
    let swap = array[i];
    array[i] = array[len - i - 1];
    array[len - i - 1] = swap;
  }
  return array;
}

console.log(reverseArray(array1));


